#!/usr/bin/perl
#
#comparison operators are aslo called relational operators
$x="hello";
$y="apple";

if($x lt $y)
{
        print"$x less then $y\n"; #lt---> less then
}
if($x gt $y)
{
        print"$x greater then  $y\n";  #gt --->greater then
}

if($x  le  $y)
{
        print"$x is less then $y\n";  #le ---->less then are equal to 
}

if($x ge $y)
{
        print"$x is greater then or equal to  $y\n"; #ge ----> greater then or  equal to
}
if($x eq $y)
{
        print"$x  equal to $y\n"; #eq------>equal to

}

if($x ne $y)
{
        print"$x not same  $y\n"; #ne ----> not same
}

